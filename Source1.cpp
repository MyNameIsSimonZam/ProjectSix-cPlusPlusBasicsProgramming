/* Задача 1. Банкомат 4.0.
В банке ПлюсПлюсБанк решили улучшить новую систему пин - кодов на банкоматы.
Пользователям оказалось неудобно вводить 4 отдельных числа.
Перепишите программу так, чтобы пользователь вводил число любой длины,
а дальше все то же самое : если сумма цифр числа оказывается равна 42,
пользователь может продолжать работу.
Напишите программу, которая спрашивает у пользователя число и отвечает “Ввод корректный”,
если сумма его цифр равна 42, и “Ввод некорректный” в противном случае.
При этом у пользователя есть только одна попытка ввести корректное число.
*/


void task17() {
	int pin;
	int res = 0;
	std::cout << "Enter the pin: ";
	std::cin >> pin;

	while (pin != 0)
	{
		res += pin % 10;
		pin /= 10;
		if (res > 42) break;
	}
	if (res == 42) std::cout << "pin is correct\n";
	else std::cout << "pin is incorrect\n";
}

/*Задача 2. Кадастровые проблемы.
В столице программистов на квадратные участки дается скидка.
А так как работать с целыми числами программисты начинают раньше,
то и участки, площадь которых является точным квадратом(например, 25 или 49),
являются самыми востребованными.Напишите программу, которая проверяет,
является ли введенная площадь участка точным квадратом.
Самый большой участок в городе имеет площадь в 1 000 000 м2.*/

void task18() {
	int s;
	int res = 1;
	int count = 1;
	std::cout << "Enter the land area: ";
	std::cin >> s;
	if (s > 1000000) {
		std::cout << "We don't have so big a land plot\n";
		return;
	}
	while (s > res) {
		res = count * count;
		if (res == s) {
			std::cout << "Square land plot\n";
			break;
		}
		++count;
		if (res > s) std::cout << "The land plot isn't square\n";
	}
}


