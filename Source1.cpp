/*Задача 4. Военное время.
Во времена Второй Мировой в штабах использовались стеклянные доски.
С одной их стороны специальные люди писали сводки с фронта, а с другой генералы их читали.
Как вы понимаете, для генералов текст выглядел зеркально отраженным,
поэтому специальных людей учили писать задом наперед.
Напишите программу, упрощающую им жизнь : она должна выводить введенное число задом наперед.*/

void task20() {
	int n;
	std::cout << "Enter the number ";
	std::cin >> n;
	int reversed = n % 10;
	while (n!=0) {
		n = n / 10;
		if (n == 0) break;
		reversed *= 10;
		reversed += n % 10;
	}
	std::cout << reversed << "\n";
}






